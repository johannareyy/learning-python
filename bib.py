def produto(x,y):
    """
    Calcula o produto entre dois números
    Recebe: dois números reais
    Retorna: o produto
    """
    return x*y

def soma(x,y):
    """
    Calcula a soma entre dois números
    Recebe: dois números reais
    Retorna: o produto
    """
    return x+y

